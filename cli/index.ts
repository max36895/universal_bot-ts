/**
 * # Консольный скрипт
 *
 * ## Назначение
 *
 * Скрипт предназначен для быстрого создания приложения.
 * Он способен создать шаблон приложения.
 *
 * ## Запуск
 *
 * Чтобы воспользоваться скриптом, запустите файл umbot.js, и передайте необходимые параметры.
 * ```bash
 * npm umbot
 * ```
 *
 * ## Команды
 *
 * На данный момент поддерживаются команды:
 * - create — создать проект
 * - generateEnv — сгенерировать пустой .env файл
 *
 * При создании проекта вторым аргументом нужно передать либо название проекта на английском языке, либо JSON-файл с конфигурацией.
 * При передаче json файла, можно создать шаблон приложения определенного типа. Сейчас поддерживается `quiz` - викторина, и пустой проект.
 *
 * Минимальный пример JSON-конфигурации (поля и типы — в README CLI, раздел «Конфигурационный файл»):
 * ```json
 * {
 *   "name": "my-bot",
 *   "type": "default",
 *   "mode": "dev",
 *   "path": "./my-bot",
 *   "config": {
 *     "json": "./data",
 *     "error_log": "./logs",
 *     "isLocalStorage": true
 *   },
 *   "isEnv": true
 * }
 * ```
 * Поле `name` обязательно; остальные — по необходимости.
 *
 * ## Пример использования
 * Создание пустого проекта:
 * ```bash
 * npx umbot create project
 * ```
 * Создание проекта, используя в качестве параметра json
 * ```json
 * {
 *  "name": "project",
 *  "type": "quiz"
 * }
 * ```
 * ```bash
 * npx umbot create project.json
 * ```
 */
