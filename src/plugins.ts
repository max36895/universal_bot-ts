/**
 * Модуль с адаптерами, который позволяет создавать навыки для Алисы, чат-ботов и другие приложения, работающие на разных платформах. Включает адаптеры для подключения голосовых платформ (Алиса, Маруся, Сбер SmartApp) и мессенджеров (Telegram, Viber, VK), а также для работы с базами данных.
 *
 * ## 📦 Что входит в модуль
 * - **Адаптеры платформ**: Алиса, Маруся, Сбер.SmartApp, Telegram, Viber, VK, MAX и другие.
 * - **Адаптеры баз данных**: MongoDB, файловая БД (для разработки).
 * - **Готовые наборы**: {@link voicePlatforms}, {@link botPlatforms}, {@link fullPlatforms} — подключают сразу все нужные платформы одной строкой.
 *
 * ## 🚀 Рекомендации
 *  - Не рекомендуется поштучно регистрировать необходимые адаптеры для платформы, лучше использовать готовые методы:
 *    - {@link voicePlatforms} - для подключения только голосовых платформ.
 *    - {@link botPlatforms} - для подключения платформ для чат-ботов.
 *    - {@link fullPlatforms} - для подключения всех доступных платформ.
 *    Подключать платформы по отдельности рекомендуется только в том случае, если вы явно хотите ограничить количество поддерживаемых платформ.
 *  - Для добавления своей платформы, отнаследуйтесь от {@link BasePlatformAdapter}, и настройте адаптер согласно документации.
 *  - Использовать файловую базу данных только во время тестирования, или для очень маленьких приложений. Например, личный чат бот или чат бот для команды до 100 человек.
 *  - Для добавления своей работы с базой данных, отнаследуйтесь от {@link BaseDbAdapter}, и настройте адаптер согласно документации.
 *
 * @example
 * ```ts
 * import { Bot } from 'umbot';
 * import { fullPlatforms } from 'umbot/plugins';
 *
 * const bot = new Bot()
 *   .use(fullPlatforms) // подключает все доступные платформы сразу
 *   .start(3000);
 * ```
 *
 * @author Maxim-M <maximco36895@yandex.ru>
 * @packageDocumentation
 * @module plugins
 */
